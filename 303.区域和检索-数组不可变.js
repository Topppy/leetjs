/**
 * @param {number[]} nums
 */
var NumArray = function(nums) {
  const memo =[0]
  for(let i =1; i<nums.length+1; i++) {
    memo[i] = nums[i-1] + memo[i-1] || 0
  }
  this.memo = memo
};

/** 
 * @param {number} left 
 * @param {number} right
 * @return {number}
 */
NumArray.prototype.sumRange = function(left, right) {
  return this.memo[right + 1] - this.memo[left]
};

